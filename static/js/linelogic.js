function yScale(chartData, height) {
  // create scales
  var yLinearScale = d3.scaleLinear()
    .domain([d3.min(chartData, d => d.Value),
      d3.max(chartData, d => d.Value)
    ])
    .range([height, 0]);
  return yLinearScale;
  }
  
